let n = parseInt(prompt("Digite um número inteiro:")); //O usuário vai digitar o número desejado
const soma = (n * (n + 1)) / 2; //O programa vai calcular a soma dos números naturais  não nulos até o número digitado

document.write("<br><br><br><br><br><br><br><br>O resultado da soma dos ℕ* de 1 até " + n + " é: " + soma); //O programa vai exibir o resultado da soma na tela